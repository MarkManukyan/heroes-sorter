import sortHeroes from '../sortHeroes';

test('sorts heroes by health in descending order', () => {
    const heroes = [
        { name: 'мечник', health: 10 },
        { name: 'маг', health: 100 },
        { name: 'лучник', health: 80 },
    ];
    const sorted = sortHeroes(heroes);
    expect(sorted).toEqual([
        { name: 'маг', health: 100 },
        { name: 'лучник', health: 80 },
        { name: 'мечник', health: 10 },
    ]);
});